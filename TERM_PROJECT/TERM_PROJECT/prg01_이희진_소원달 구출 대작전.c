bool mini_game_over(const int cactus_x, const int boss_y) {	// 게임오버 조건
	if (cactus_x <= 8 && cactus_x >= 4 && boss_y > 8) {
		return true;
	}
	return false;
}

int key() {
	if (_kbhit() != 0) {
		return _getch();
	}
	return 0;
}

void mini_game_main() {
	system("mode con:cols=100 lines=25");
	system("title mini_game");

	int boss_y = mini_game_boss_y;
	int cactus_x = mini_game_cactus_x;
	int score = 0;
	const int z = 3;
	bool jump = false;
	bool end = true;
	clock_t start_time, time;
	start_time = clock();

	while (true) {
		if (mini_game_over(cactus_x, boss_y))
			break;

		if (key() == 32 && end) {		// 32는 스페이스키
			jump = true;
			end = false;
		}
		if (jump) {
			boss_y = boss_y - z;
		}
		else {
			boss_y = boss_y + z;
		}
		if (boss_y >= mini_game_boss_y) {
			boss_y = mini_game_boss_y;
			end = true;
		}
		cactus_x = cactus_x - 2;
		if (cactus_x <= 0) {
			cactus_x = mini_game_cactus_x;
		}
		if (boss_y <= 3) {
			jump = false;
		}

		mini_game_boss(boss_y);
		mini_game_cactus(cactus_x);

		time = clock();
		if (((time - start_time) / CLOCKS_PER_SEC) >= 1) {
			score++;
			start_time = clock();
		}

		Sleep(20);
		system("cls");
		x_y(42, 0);   
		printf("Score : %d ", score);    //점수 출력해줌.
	}
	mini_game_gameover(score);
}

